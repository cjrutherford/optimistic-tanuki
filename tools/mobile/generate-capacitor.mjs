#!/usr/bin/env node
/**
 * Generates Capacitor mobile scaffolding for Angular client apps declared in the
 * registry YAML (tools/registry/apps.production.sample.yaml by default).
 *
 * For every app with `appType: client` and `mobile.enabled: true` this writes:
 *   - apps/<project>/capacitor.config.ts        (always regenerated)
 *   - apps/<project>/src/environments/app-env.mobile.ts (always regenerated)
 *   - apps/<project>/src/environments/app-env.ts        (created only if missing)
 *
 * The Nx project name defaults to the registry appId; override with mobile.project.
 *
 * Usage:
 *   node tools/mobile/generate-capacitor.mjs [--registry <path>] [--check]
 *
 * --check exits non-zero if any generated file is missing or out of date,
 * without writing anything (for CI).
 */
import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { resolve, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

const require = createRequire(import.meta.url);
const yaml = require('js-yaml');

const workspaceRoot = resolve(
  dirname(fileURLToPath(import.meta.url)),
  '..',
  '..'
);

const args = process.argv.slice(2);
const checkMode = args.includes('--check');
const registryFlag = args.indexOf('--registry');
const registryPath =
  registryFlag !== -1
    ? resolve(args[registryFlag + 1])
    : join(workspaceRoot, 'tools', 'registry', 'apps.production.sample.yaml');

const registry = yaml.load(readFileSync(registryPath, 'utf8'));
const apps = (registry.apps ?? []).filter(
  (app) => app.appType === 'client' && app.mobile?.enabled
);

if (apps.length === 0) {
  console.log(`No mobile-enabled client apps found in ${registryPath}`);
  process.exit(0);
}

const header = (source) =>
  `// GENERATED by tools/mobile/generate-capacitor.mjs from ${source} — do not edit by hand.\n`;

const capacitorConfig = (app, project) => {
  const appName = app.mobile.appName ?? app.name;
  return `${header(
    'the app registry'
  )}import type { CapacitorConfig } from '@capacitor/cli';

const config: CapacitorConfig = {
  appId: '${app.mobile.bundleId}',
  appName: ${JSON.stringify(appName)},
  webDir: '../../dist/apps/${project}/browser',
  server: {
    androidScheme: 'https',
    // Dev-mode live reload: point the webview at a dev server instead of bundled assets.
    ...(process.env['CAP_SERVER_URL']
      ? { url: process.env['CAP_SERVER_URL'], cleartext: true }
      : {}),
  },
};

export default config;
`;
};

const mobileEnv = (app) => {
  const uiBase = (app.uiBaseUrl ?? '').replace(/\/$/, '');
  const apiBase = (app.apiBaseUrl ?? `${uiBase}/api`).replace(/\/$/, '');
  return `${header('the app registry')}export const APP_ENV = {
  mobile: true,
  apiBaseUrl: '${apiBase}',
  socketUrl: '${uiBase}',
  socketPath: '/socket.io',
};
`;
};

const defaultEnv = `// Default (web) runtime environment. The mobile build replaces this file with
// app-env.mobile.ts via fileReplacements in the app's "mobile" build configuration.
export const APP_ENV = {
  mobile: false,
  apiBaseUrl: '/api',
  socketUrl: '',
  socketPath: '/socket.io',
};
`;

let failed = false;
let wrote = 0;

function emit(path, content, { skipIfExists = false } = {}) {
  const exists = existsSync(path);
  if (skipIfExists && exists) return;
  const current = exists ? readFileSync(path, 'utf8') : null;
  if (current === content) return;
  if (checkMode) {
    console.error(`OUT OF DATE: ${path}`);
    failed = true;
    return;
  }
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, content);
  console.log(`wrote ${path}`);
  wrote++;
}

for (const app of apps) {
  const project = app.mobile.project ?? app.appId;
  const appDir = join(workspaceRoot, 'apps', project);
  if (!existsSync(appDir)) {
    console.error(
      `Registry app "${app.appId}" maps to missing Nx project dir apps/${project} (set mobile.project to override).`
    );
    failed = true;
    continue;
  }
  emit(join(appDir, 'capacitor.config.ts'), capacitorConfig(app, project));
  emit(
    join(appDir, 'src', 'environments', 'app-env.mobile.ts'),
    mobileEnv(app)
  );
  emit(join(appDir, 'src', 'environments', 'app-env.ts'), defaultEnv, {
    skipIfExists: true,
  });
}

if (failed) process.exit(1);
console.log(
  checkMode
    ? 'All generated mobile files are up to date.'
    : `Done. ${wrote} file(s) written for ${apps.length} app(s).`
);
